export type ButtonVariant =
  | "primary" | "ghost-dark" | "outline-light" | "soon"
  | "danger" | "danger-ghost";

export interface ButtonProps {
  variant?: ButtonVariant;
  children?: ReactNode;
  arrow?: boolean;
  icon?: string;
  as?: "button" | "a";
  href?: string;
  onClick?: () => void;
  type?: "button" | "submit" | "reset";
  disabled?: boolean;
  style?: CSSProperties;
}

export function Button({
  variant = "primary", children, arrow, icon, as = "button", href, onClick, type, disabled, style,
}: ButtonProps) {
  const cls = `btn btn-${variant}`;
  const inner = (
    <>
      {icon && <Icon name={icon} size={15} stroke={2} />}
      {children}
      {arrow && <Icon name="arrow-right" size={14} stroke={2} />}
    </>
  );
  if (as === "a") {
    return <a className={cls} href={href || "#"} onClick={onClick} style={style}>{inner}</a>;
  }
  return <button className={cls} onClick={onClick} type={type} disabled={disabled} style={style}>{inner}</button>;
}

